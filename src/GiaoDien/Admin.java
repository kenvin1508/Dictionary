    public void insert() {
        tu = jTFTu.getText();
        pa = jTFPAm.getText();
        lt = jTFLoaiTu.getText();
        ng = jTFNghia.getText();
        am = jTFAm.getText();
        anh = jTFAnh.getText();
        MySQL.adInsert(tu, pa, lt, ng, am, anh);

    }

    public void delete() {
        tu = jTFTu.getText();
        MySQL.adDelete(tu);
    }

    public void update() {
        tu = jTFTu.getText();
        pa = jTFPAm.getText();
        lt = jTFLoaiTu.getText();
        ng = jTFNghia.getText();
        am = jTFAm.getText();
        anh = jTFAnh.getText();
        id = Integer.parseInt(jTFId.getText());
        MySQL.adUpdate(tu, pa, lt, ng, am, anh, id);

    }

