    public void showDataInTable() {

        ArrayList<Data> list = MySQL.getUsersList();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] row = new Object[7];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getTu();
            row[2] = list.get(i).getpAm();
            row[3] = list.get(i).getlTu();
            row[4] = list.get(i).getNg();
            row[5] = list.get(i).getAm();
            row[6] = list.get(i).getAnh();

            model.addRow(row);
        }
    }
